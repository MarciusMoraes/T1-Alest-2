public class Node {
        int value;
        Node left;    // Filho à esquerda
        Node middle;  // Filho no meio
        Node right;   // Filho à direita
    
        // Construtor do nó
        public Node(int value) {
            this.value = value;
            this.left = null;
            this.middle = null;
            this.right = null;
        }
    }

